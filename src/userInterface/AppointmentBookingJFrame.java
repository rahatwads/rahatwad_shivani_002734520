    private void btnBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBookActionPerformed
        // TODO add your handling code here:
        if(txtHospital.getText().equals("") || txtPid.getText().equals("") ||txtPatientname.getText().equals("") || txtDocName.getText().equals("")|| txtdt.getText().equals("")|| txttime.getText().equals("")){
           JOptionPane .showMessageDialog(this, "Please Enter all data")  ; 
        } else{
            String data[] = {txtHospital.getText(),txtPid.getText(),txtPatientname.getText(),txtDocName.getText(),txtdt.getText(),txttime.getText()};
        
            DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
            tblModel.addRow(data);
            JOptionPane .showMessageDialog(this, "Booked successfully")  ; 
            txtHospital.setText("");
            txtPid.setText("");
            txtPatientname.setText("");
            txtDocName.setText("");
            txtdt.setText("");
            txttime.setText("");
        }
        
            
    }//GEN-LAST:event_btnBookActionPerformed

